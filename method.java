public class method {

    /*
     * access_type static return_type/void method_name(parameters){
     * all code for completing method work;
     * return;
     * }
     */

    // add method
    public static int add(int a, int b) {
        int sum = a + b;
        return sum;
    }

    // main method
    public static void main(String[] args) {
        System.out.println(add(7, 9));
    }
}
